<section>
    <h2>1. การยอมรับข้อตกลง</h2>
    <p>ยินดีต้อนรับสู่ Darknight Social ในการสร้างบัญชี ดาวน์โหลดแอปพลิเคชันมือถือของเรา หรือใช้ Darknight Social ("บริการ") ของเรา ถือว่าคุณตกลงที่จะผูกพันตามข้อตกลงการให้บริการเหล่านี้ ("ข้อตกลง") หากคุณไม่ตกลงตามข้อตกลงเหล่านี้ คุณจะไม่สามารถใช้บริการได้ ข้อตกลงเหล่านี้ถือเป็นสัญญาที่มีผลผูกพันตามกฎหมายระหว่างคุณและ Darknight Social</p>
</section>

<section>
    <h2>2. คุณสมบัติของผู้ใช้</h2>
    <p>คุณต้องมีอายุอย่างน้อย 13 ปี (หรืออายุขั้นต่ำตามกฎหมายในประเทศของคุณ) เพื่อใช้บริการของเรา ในการใช้ Darknight คุณรับรองและรับประกันว่าคุณมีคุณสมบัติตามข้อกำหนดนี้ มีความสามารถตามกฎหมายในการเข้าทำข้อตกลงเหล่านี้ และการใช้บริการของคุณไม่ละเมิดกฎหมายหรือข้อบังคับใดๆ ที่เกี่ยวข้อง</p>
</section>

<section>
    <h2>3. บัญชีผู้ใช้</h2>
    <p>ในการเข้าถึงคุณลักษณะบางอย่างของบริการ คุณต้องสร้างบัญชี คุณตกลงที่จะให้ข้อมูลที่ถูกต้อง เป็นปัจจุบัน และครบถ้วนในระหว่างขั้นตอนการลงทะเบียน และจะปรับปรุงข้อมูลดังกล่าวให้ถูกต้อง เป็นปัจจุบัน และครบถ้วนอยู่เสมอ คุณมีหน้าที่รับผิดชอบแต่เพียงผู้เดียวในการรักษาความลับของชื่อผู้ใช้และรหัสผ่านของคุณ และสำหรับกิจกรรมทั้งหมดที่เกิดขึ้นภายใต้บัญชีของคุณ</p>
    <p>Darknight จะไม่รับผิดชอบต่อความสูญเสียหรือความเสียหายใดๆ ที่เกิดจากการที่คุณไม่ปฏิบัติตามภาระผูกพันด้านความปลอดภัยเหล่านี้ คุณต้องแจ้งให้เราทราบทันทีหากมีการใช้งานบัญชีของคุณโดยไม่ได้รับอนุญาต</p>
    <p>เราขอแนะนำให้เปิดใช้งาน <strong>การยืนยันตัวตนแบบสองปัจจัย (2FA)</strong> ในการตั้งค่าบัญชีของคุณเพื่อเพิ่มระดับความปลอดภัยไปอีกขั้น</p>
</section>

<section>
    <h2>4. เนื้อหาของผู้ใช้และการคุ้มครองความเป็นส่วนตัว</h2>
    <p><strong>ความเป็นเจ้าของและการใช้งาน:</strong> คุณเป็นเจ้าของสิทธิ์ในเนื้อหาที่คุณโพสต์ อัปโหลด หรือจัดให้มีขึ้นผ่านบริการ ("เนื้อหาของผู้ใช้") เฉพาะคุณและ Darknight Social เท่านั้นที่ได้รับอนุญาตให้ใช้เนื้อหาของผู้ใช้ของคุณ การใช้งานของ Darknight Social จะถูกจำกัดอย่างเคร่งครัดที่การดำเนินงานและการปรับปรุงฟังก์ชันการทำงานของแพลตฟอร์มและประสบการณ์ของผู้ใช้</p>
    <p><strong>ข้อห้ามด้าน AI ที่เคร่งครัด:</strong> Darknight Social จะ <strong>ไม่ใช้</strong> เนื้อหาของผู้ใช้ของคุณเพื่อวัตถุประสงค์ในการสร้างโดย AI, การฝึกอบรมการเรียนรู้ของเครื่อง หรือการสร้างสื่อสังเคราะห์ เรามุ่งมั่นที่จะปกป้องความคิดสร้างสรรค์ของมนุษย์</p>
    <p><strong>ข้อจำกัดของผู้ให้บริการภายนอก:</strong> ห้ามมิให้ AI ของบุคคลที่สามใดๆ รวมถึงเครื่องมือดึงข้อมูลเว็บ (web scrapers), โปรแกรมรวบรวมข้อมูล หรือโมเดล AI ภายนอก ใช้เนื้อหาของผู้ใช้ของคุณโดยเด็ดขาด และไม่ได้รับอนุญาตภายใต้สัญญานี้</p>
</section>

<section>
    <h2>5. นโยบายเนื้อหาที่สร้างโดย AI</h2>
    <p><strong>ข้อห้ามที่เคร่งครัด:</strong> Darknight Social เป็นสถานพำนักพิเศษสำหรับความคิดสร้างสรรค์ของมนุษย์และการแสดงออกที่แท้จริง ห้ามโพสต์เนื้อหาที่สร้างโดย AI ทุกรูปแบบ (รวมถึงแต่ไม่จำกัดเพียงภาพ ข้อความ รหัส หรือสื่อสังเคราะห์ที่สร้างโดย AI) โดยเด็ดขาดและสมบูรณ์</p>
    <p><strong>การไม่ยอมรับโดยสิ้นเชิง (Zero Tolerance):</strong> เรามีนโยบายไม่ยอมรับ AI โดยสิ้นเชิง เนื้อหาใดๆ ที่ถูกตรวจพบหรือได้รับรายงานว่าสร้างโดย AI จะถูกลบออกทันทีโดยไม่ต้องแจ้งให้ทราบล่วงหน้า การใช้ AI เพื่อเลียนแบบศิลปะของมนุษย์ หรือเนื้อหา "ที่ได้รับความช่วยเหลือจาก AI" ซึ่งพึ่งพาโมเดลเชิงสร้างสรรค์อย่างหนักก็เป็นสิ่งต้องห้ามเช่นกัน</p>
    <p><strong>บทลงโทษบัญชี:</strong> การพยายามหลีกเลี่ยงนโยบายนี้ซ้ำๆ หรือการใช้บอทอัตโนมัติเพื่อโพสต์เนื้อหาจะส่งผลให้มีการปิดบัญชีทันทีและถาวร เราเชื่อในการรักษาความศักดิ์สิทธิ์ของศิลปะที่มนุษย์สร้างขึ้นและการปฏิสัมพันธ์ของมนุษย์อย่างแท้จริง</p>
</section>

<section>
    <h2>6. เนื้อหา NSFW และสปอยเลอร์</h2>
    <p><strong>นโยบาย NSFW:</strong> เนื้อหาที่ไม่เหมาะสมสำหรับการดูในที่ทำงาน (NSFW) จะได้รับอนุญาตในบริการสำหรับผู้ใหญ่ โดยต้องเป็นเนื้อหาที่ถูกกฎหมายทั้งในเขตอำนาจศาลของผู้ส่งและผู้รับ เนื้อหา NSFW ทั้งหมด <strong>ต้อง</strong> ถูกระบุว่าเป็น "สปอยเลอร์" หรือทำให้เบลอโดยใช้เครื่องมือที่แพลตฟอร์มจัดเตรียมไว้ให้</p>
    <p><strong>ข้อจำกัดด้านอายุ:</strong> การเข้าถึงเนื้อหา NSFW จำกัดไว้สำหรับผู้ใช้ที่มีอายุ 18 ปีขึ้นไปเท่านั้น ผู้ใช้ที่มีอายุต่ำกว่า 18 ปีจะเห็นเนื้อหา NSFW/สปอยเลอร์ <strong>ถูกซ่อนไว้โดยสมบูรณ์และไม่สามารถเข้าถึงได้</strong> ภายในบริการ นอกจากนี้ ผู้ใช้ที่มีอายุต่ำกว่า 18 ปีจะถูกระงับการอัปโหลดหรือแบ่งปันเนื้อหาใดๆ ที่มีการระบุว่าเป็น NSFW หรือสปอยเลอร์</p>
    <p><strong>การบังคับใช้:</strong> การไม่ระบุเนื้อหา NSFW อย่างถูกต้อง การให้ข้อมูลอายุอันเป็นเท็จเพื่อหลีกเลี่ยงข้อจำกัด หรือการโพสต์เนื้อหาลามกอนาจารที่ผิดกฎหมาย จะส่งผลให้มีการยุติบัญชีทันทีและถาวร</p>
</section>

<section>
    <h2>7. แนวทางปฏิบัติของชุมชนและพฤติกรรมที่ต้องห้าม</h2>
    <p>เพื่อรักษาสภาพแวดล้อมที่ปลอดภัย คุณตกลงที่จะไม่:</p>
    <ul>
        <li>โพสต์เนื้อหาที่ผิดกฎหมาย เป็นอันตราย คุกคาม หมิ่นประมาท หรือหยาบคาย</li>
        <li>มีส่วนร่วมในการลอบติดตาม กลั่นแกล้ง หรือเปิดเผยข้อมูลส่วนบุคคล (doxing) ของผู้ใช้อื่น</li>
        <li>อัปโหลดไวรัส มัลแวร์ หรือโค้ดที่เป็นอันตราย</li>
        <li>พยายามเข้าถึงระบบของเราหรือบัญชีของผู้ใช้อื่นโดยไม่ได้รับอนุญาต</li>
        <li>โพสต์เนื้อหาที่สร้างโดย AI ทุกประเภท (รูปภาพ ข้อความ หรือสื่อสังเคราะห์)</li>
        <li>โพสต์เนื้อหา NSFW หรือความรุนแรงโดยไม่มีการระบุเครื่องหมาย</li>
        <li>แอบอ้างเป็นบุคคลหรือหน่วยงานใดๆ</li>
        <li>แบ่งปัน "สแปม" ธุรกิจเครือข่าย หรือโฆษณาที่ไม่ได้รับอนุญาต</li>
    </ul>
</section>

<section>
    <h2>8. ทรัพย์สินทางปัญญา</h2>
    <p>สิทธิ์ กรรมสิทธิ์ และผลประโยชน์ทั้งหมดในบริการ (ยกเว้นเนื้อหาของผู้ใช้) เป็นและจะยังคงเป็นทรัพย์สินของ Darknight Social และผู้อนุญาตแต่เพียงผู้เดียว บริการนี้ได้รับความคุ้มครองตามกฎหมายลิขสิทธิ์ เครื่องหมายการค้า และกฎหมายอื่นๆ</p>
</section>

<section>
    <h2>9. ความเป็นส่วนตัว</h2>
    <p>ความเป็นส่วนตัวของคุณเป็นสิ่งสำคัญที่สุด นโยบายความเป็นส่วนตัวของเราอธิบายถึงวิธีการที่เราจัดการข้อมูลที่คุณให้ไว้เมื่อคุณใช้บริการของเรา คุณเข้าใจว่าในการใช้บริการของเรา คุณตกลงให้มีการรวบรวมและใช้ข้อมูลนี้ตามที่ระบุไว้ในนโยบายความเป็นส่วนตัว</p>
</section>

<section>
    <h2>10. ลิงก์และบริการของบุคคลที่สาม</h2>
    <p>บริการอาจมีลิงก์ไปยังเว็บไซต์หรือบริการของบุคคลที่สามที่ไม่ได้เป็นของหรือควบคุมโดย Darknight Social เราไม่รับรองหรือรับผิดชอบต่อเว็บไซต์ ข้อมูล วัสดุ ผลิตภัณฑ์ หรือบริการของบุคคลที่สามดังกล่าว หากคุณเข้าถึงเว็บไซต์ของบุคคลที่สามจากบริการนี้ คุณต้องรับความเสี่ยงด้วยตนเอง</p>
</section>

<section>
    <h2>11. การยกเลิกบริการ</h2>
    <p>เราอาจยกเลิกหรือระงับการเข้าถึงบริการของคุณทันที โดยไม่ต้องแจ้งให้ทราบล่วงหน้าหรือรับผิดชอบต่อความเสียหายใดๆ ด้วยเหตุผลใดก็ตาม รวมถึงแต่ไม่จำกัดเพียงกรณีที่คุณละเมิดข้อตกลง เมื่อยกเลิกแล้ว สิทธิ์ของคุณในการใช้บริการจะสิ้นสุดลงทันที</p>
</section>

<section>
    <h2>12. การจำกัดความรับผิด</h2>
    <p>ภายใต้ขอบเขตสูงสุดที่กฎหมายอนุญาต Darknight Social จะไม่รับผิดชอบต่อความเสียหายทางอ้อม ความเสียหายที่เกิดขึ้นโดยไม่ได้ตั้งใจ ความเสียหายพิเศษ หรือความเสียหายที่เป็นผลต่อเนื่อง หรือการสูญเสียผลกำไรหรือรายได้ ไม่ว่าจะเกิดขึ้นโดยตรงหรือโดยอ้อม หรือการสูญเสียข้อมูล การใช้งาน ค่าความนิยม หรือการสูญเสียที่จับต้องไม่ได้อื่นๆ ที่เกิดจาก (i) การเข้าถึงหรือการใช้ หรือการไม่สามารถเข้าถึงหรือใช้บริการของคุณ (ii) พฤติกรรมหรือเนื้อหาของบุคคลที่สามในบริการ หรือ (iii) การเข้าถึง การใช้งาน หรือการแก้ไขการรับส่งข้อมูลหรือเนื้อหาของคุณโดยไม่ได้รับอนุญาต</p>
</section>

<section>
    <h2>13. การชดใช้ค่าเสียหาย</h2>
    <p>คุณตกลงที่จะชดใช้ค่าเสียหายและปกป้อง Darknight Social และบริษัทในเครือ เจ้าหน้าที่ กรรมการ พนักงาน และตัวแทนจากข้อเรียกร้อง ความเสียหาย ภาระผูกพัน ความสูญเสีย ความรับผิด ค่าใช้จ่าย หรือหนี้สิน และค่าใช้จ่ายใดๆ (รวมถึงแต่ไม่จำกัดเพียงค่าทนายความ) ที่เกิดจาก: (i) การใช้และการเข้าถึงบริการของคุณ (ii) การละเมิดข้อตกลงใดๆ เหล่านี้ของคุณ หรือ (iii) การละเมิดสิทธิ์ของบุคคลที่สามโดยคุณ</p>
</section>

<section>
    <h2>14. กฎหมายที่บังคับใช้</h2>
    <p>ข้อตกลงเหล่านี้จะอยู่ภายใต้การควบคุมและตีความตามกฎหมายของเขตอำนาจศาลที่ดำเนินการ โดยไม่คำนึงถึงความขัดแย้งของหลักกฎหมาย</p>
</section>

<section>
    <h2>15. การเปลี่ยนแปลงข้อตกลง</h2>
    <p>เราขอสงวนสิทธิ์ในการแก้ไขหรือเปลี่ยนข้อตกลงเหล่านี้ได้ตลอดเวลาตามดุลยพินิจของเราแต่เพียงผู้เดียว เราจะแจ้งให้ทราบหากมีการเปลี่ยนแปลงที่สำคัญ การเข้าถึงหรือใช้บริการของเราต่อไปหลังจากที่การแก้ไขดังกล่าวมีผลบังคับใช้ ถือว่าคุณตกลงที่จะผูกพันตามข้อตกลงที่แก้ไขใหม่</p>
</section>

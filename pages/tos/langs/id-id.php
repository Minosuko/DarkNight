</section>

<section>
    <h2>4. Konten Pengguna & Perlindungan Privasi</h2>
    <p><strong>Kepemilikan & Penggunaan:</strong> Anda memiliki hak atas konten yang Anda posting, unggah, atau sediakan melalui Layanan ("Konten Pengguna"). Hanya Anda dan Darknight Social yang berwenang untuk menggunakan Konten Pengguna Anda. Penggunaan Darknight Social sangat terbatas pada pengoperasian dan peningkatan fungsionalitas platform serta pengalaman pengguna.</p>
    <p><strong>Larangan AI yang Ketat:</strong> Darknight Social <strong>TIDAK AKAN PERNAH</strong> menggunakan Konten Pengguna Anda untuk tujuan generatif AI, pelatihan machine learning, atau pembuatan media sintetis. Kami berkomitmen untuk melindungi kreativitas manusia.</p>
    <p><strong>Batasan Pihak Ketiga:</strong> Penggunaan Konten Pengguna Anda oleh AI pihak ketiga mana pun, termasuk web scraper, crawler data, atau model AI eksternal, sangat dilarang dan tidak diizinkan oleh lisensi ini.</p>
</section>

<section>
    <h2>5. Kebijakan Konten Buatan AI</h2>
    <p><strong>Larangan Keras:</strong> Darknight Social adalah tempat perlindungan eksklusif bagi kreativitas manusia dan ekspresi otentik. Pemostingan konten buatan AI apa pun (termasuk namun tidak terbatas pada gambar, teks, kode, atau media sintetis buatan AI) sangat dilarang keras dan mutlak.</p>
    <p><strong>Nol Toleransi:</strong> Kami menerapkan kebijakan nol toleransi terhadap AI. Konten apa pun yang terdeteksi atau dilaporkan sebagai buatan AI akan segera dihapus tanpa pemberitahuan sebelumnya. Penggunaan AI untuk meniru seni manusia atau konten "dibantu AI" yang sangat bergantung pada model generatif juga dilarang.</p>
    <p><strong>Sanksi Akun:</strong> Upaya berulang untuk mengelabui kebijakan ini atau penggunaan bot otomatis untuk memposting konten akan mengakibatkan penutupan akun segera dan permanen. Kami percaya pada upaya menjaga kesucian seni buatan manusia dan interaksi manusia yang tulus.</p>
</section>

<section>
    <h2>6. Konten NSFW & Spoiler</h2>
    <p><strong>Kebijakan NSFW:</strong> Konten Not Safe For Work (NSFW) diizinkan di Layanan untuk pengguna dewasa, asalkan legal di yurisdiksi pengirim dan penerima. Semua konten NSFW <strong>HARUS</strong> ditandai secara eksplisit sebagai "Spoiler" atau diburamkan menggunakan alat yang disediakan platform.</p>
    <p><strong>Batasan Usia:</strong> Akses ke konten NSFW sangat dibatasi untuk pengguna berusia 18 tahun ke atas. Pengguna di bawah usia 18 tahun akan melihat konten NSFW/Spoiler <strong>sepenuhnya tersembunyi dan tidak dapat diakses</strong> dalam Layanan. Selain itu, pengguna di bawah usia 18 tahun dilarang mengunggah atau membagikan konten apa pun yang ditandai sebagai NSFW atau Spoiler.</p>
    <p><strong>Penegakan:</strong> Kegagalan untuk menandai konten NSFW dengan benar, memberikan informasi usia palsu untuk melewati batasan, atau memposting materi dewasa ilegal akan mengakibatkan penghentian akun segera dan permanen.</p>
</section>


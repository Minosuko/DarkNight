<section>
    <h2>3. Akun Pengguna</h2>
    <p>Untuk mengakses fitur tertentu dari Layanan, Anda harus membuat akun. Anda setuju untuk memberikan informasi yang akurat, terkini, dan lengkap selama proses pendaftaran dan memperbarui informasi tersebut agar tetap akurat, terkini, dan lengkap. Anda bertanggung jawab penuh atas kerahasiaan kredensial akun Anda dan semua aktivitas yang terjadi di bawah akun Anda.</p>
    <p>Darknight tidak dapat dan tidak akan bertanggung jawab atas kehilangan atau kerusakan yang timbul dari kegagalan Anda untuk mematuhi kewajiban keamanan ini. Anda harus segera memberi tahu kami tentang penggunaan akun Anda yang tidak sah.</p>

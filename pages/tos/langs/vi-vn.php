        <li>Đăng tải nội dung do AI tạo ra dưới bất kỳ hình thức nào (hình ảnh, văn bản hoặc phương tiện tổng hợp).</li>
        <li>Đăng tải nội dung NSFW hoặc bạo lực mà không có cảnh báo.</li>
        <li>Mạo danh bất kỳ cá nhân hoặc tổ chức nào.</li>
        <li>Phát tán "thư rác" (spam), mô hình đa cấp hoặc quảng cáo trái phép.</li>
    </ul>

<section>
    <h2>1. Chấp nhận các Điều khoản</h2>
    <p>Chào mừng bạn đến với Darknight Social. Bằng việc tạo tài khoản, tải ứng dụng hoặc sử dụng Darknight Social ("Dịch vụ"), bạn đồng ý tuân thủ các Điều khoản Dịch vụ này ("Điều khoản"). Nếu không đồng ý, vui lòng không sử dụng Dịch vụ. Các Điều khoản này thiết lập một thỏa thuận pháp lý có tính ràng buộc giữa bạn và Darknight Social.</p>
</section>

<section>
    <h2>2. Điều kiện Sử dụng</h2>
    <p>Bạn phải từ đủ 13 tuổi trở lên (hoặc đủ độ tuổi tối thiểu theo quy định pháp luật tại quốc gia của bạn) để sử dụng Dịch vụ. Bằng việc sử dụng Darknight, bạn cam đoan và bảo đảm rằng mình đáp ứng yêu cầu này, có đầy đủ năng lực hành vi dân sự để thực hiện các Điều khoản này và việc sử dụng Dịch vụ không vi phạm bất kỳ quy định pháp luật hiện hành nào.</p>
</section>

<section>
    <h2>3. Tài khoản Người dùng</h2>
    <p>Để trải nghiệm đầy đủ các tính năng, bạn cần khởi tạo tài khoản. Bạn cam kết cung cấp thông tin chính xác, cập nhật và đầy đủ trong quá trình đăng ký, đồng thời duy trì tính chính xác của thông tin này. Bạn chịu trách nhiệm hoàn toàn về việc bảo mật thông tin đăng nhập và mọi hoạt động diễn ra dưới tài khoản của mình.</p>
    <p>Darknight không chịu trách nhiệm cho bất kỳ tổn thất hay thiệt hại nào phát sinh từ việc bạn không tuân thủ các nghĩa vụ bảo mật. Hãy thông báo ngay cho chúng tôi nếu phát hiện bất kỳ hành vi truy cập trái phép nào vào tài khoản của bạn.</p>
    <p>Chúng tôi khuyến khích bạn kích hoạt <strong>Xác thực hai lớp (2FA)</strong> trong phần cài đặt để tăng cường bảo mật cho tài khoản.</p>
</section>

<section>
    <h2>4. Nội dung Người dùng & Bảo vệ Quyền riêng tư</h2>
    <p><strong>Quyền sở hữu & Sử dụng:</strong> Bạn sở hữu toàn bộ quyền đối với nội dung mà mình đăng tải hoặc cung cấp thông qua Dịch vụ ("Nội dung Người dùng"). Chỉ bạn và Darknight Social được quyền sử dụng Nội dung Người dùng của bạn. Quyền sử dụng của Darknight Social chỉ giới hạn trong việc vận hành, duy trì và cải thiện các chức năng của nền tảng cũng như trải nghiệm người dùng.</p>
    <p><strong>Cấm Tuyệt đối AI:</strong> Darknight Social cam kết <strong>KHÔNG BAO GIỜ</strong> sử dụng Nội dung Người dùng của bạn cho các mục đích tạo nội dung bằng AI, huấn luyện học máy (machine learning) hoặc tạo ra các sản phẩm truyền thông tổng hợp. chúng tôi kiên định bảo vệ thành quả sáng tạo của con người.</p>
    <p><strong>Hạn chế Bên thứ ba:</strong> Việc sử dụng Nội dung Người dùng của bạn bởi bất kỳ bên thứ ba nào vì mục đích AI, bao gồm thu thập dữ liệu web (web scraping), bot khai thác dữ liệu hoặc các mô hình AI bên ngoài, đều bị nghiêm cấm và không được cấp phép theo thỏa thuận này.</p>
</section>

<section>
    <h2>5. Chính sách Nội dung do AI tạo ra</h2>
    <p><strong>Nghiêm cấm Tuyệt đối:</strong> Darknight Social là không gian dành riêng cho sự sáng tạo thuần túy của con người và những giá trị đích thực. Việc đăng tải bất kỳ nội dung nào do AI tạo ra (bao gồm nhưng không giới hạn ở hình ảnh, văn bản, mã nguồn hoặc các sản phẩm truyền thông tổng hợp từ AI) là hoàn toàn bị cấm.</p>
    <p><strong>Chính sách Không khoan nhượng:</strong> Chúng tôi áp dụng chính sách không khoan nhượng đối với AI. Mọi nội dung bị phát hiện hoặc bị báo cáo là sản phẩm của AI sẽ bị gỡ bỏ ngay lập tức mà không cần thông báo trước. Việc sử dụng AI để mô phỏng nghệ thuật của con người hoặc các nội dung "hỗ trợ bởi AI" nhưng phụ thuộc nặng nề vào các mô hình tạo sinh cũng không được phép.</p>
    <p><strong>Chế tài Tài khoản:</strong> Các hành vi cố tình vi phạm nhiều lần hoặc sử dụng công cụ tự động (bot) để đăng tải nội dung sẽ dẫn đến việc khóa tài khoản vĩnh viễn ngay lập tức. Chúng tôi tin vào việc bảo tồn giá trị thiêng liêng của nghệ thuật và sự kết nối chân chính giữa con người với con người.</p>
</section>

<section>
    <h2>6. Nội dung NSFW & Cảnh báo Bảo mật (Spoiler)</h2>
    <p><strong>Chính sách NSFW:</strong> Nội dung nhạy cảm (NSFW) chỉ được phép hiển thị đối với người dùng trưởng thành, với điều kiện nội dung đó hợp pháp tại cả khu vực của người gửi và người nhận. Mọi nội dung NSFW <strong>BẮT BUỘC</strong> phải được đánh dấu là "Spoiler" hoặc làm mờ bằng các công cụ tích hợp sẵn trên nền tảng.</p>
    <p><strong>Hạn chế Độ tuổi:</strong> Quyền truy cập nội dung NSFW chỉ dành cho người dùng từ 18 tuổi trở lên. Đối với người dùng dưới 18 tuổi, các nội dung NSFW/Spoiler sẽ bị <strong>ẩn hoàn toàn và không thể tiếp cận</strong>. Ngoài ra, người dùng dưới 18 tuổi không được phép tải lên hoặc chia sẻ bất kỳ nội dung nào có tính chất NSFW hoặc Spoiler.</p>
    <p><strong>Hình thức Xử lý:</strong> Việc không đánh dấu đúng nội dung NSFW, giả mạo thông tin tuổi để lách luật, hoặc đăng tải các tài liệu đồi trụy bất hợp pháp sẽ dẫn đến việc bị khóa tài khoản vĩnh viễn ngay lập tức.</p>
</section>

